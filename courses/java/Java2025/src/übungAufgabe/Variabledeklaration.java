package übungAufgabe;

public class Variabledeklaration {

	public static void main(String[] args) {
		
		double reelleZahl, reelleZahl2; //Deklaration
		char buchstable;
		reelleZahl = 1.2345; // Initialisierung
		reelleZahl2 = 4.5678;
		buchstable = 'd';
		int ganzeZahl = 4; //Deklaration + Initialisierung
		
		System.out.println("Ganze Zahl: " + ganzeZahl);
		System.out.println("reelleZah: " +  reelleZahl);
		System.out.println("reelleZah: " +  reelleZahl2);
		System.out.println("buchstable: " +  buchstable);
	}

}
